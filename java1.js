/*agrandir border*/

function bigBorder(border ,idImg) {
  border.style.height = "120px";
  border.style.width = "120px";
  idImg.style.height ="80px";
  idImg.style.width ="90px";
}

function normalBorder(border ,idImg) {
  border.style.height = "110px";
  border.style.width = "110px";
  idImg.style.height="70px";
  idImg.style.width ="80px";
}

